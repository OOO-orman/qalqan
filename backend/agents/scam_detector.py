"""
Scam Detection Agent.

Анализирует всю переписку целиком и определяет:
 - является ли это мошенничеством
 - тип схемы
 - уровень риска (0-10)
 - признаки (red flags)
"""
from llm_client import call_llm_json, LLMError

SYSTEM_PROMPT = """Ты — аналитик кибербезопасности. Тебе показывают переписку между
неизвестным собеседником и подростком/молодым человеком в Казахстане. Собеседник —
ОБЫЧНЫЙ ЧЕЛОВЕК ИЛИ ОРГАНИЗАЦИЯ, НЕ обязательно мошенник — это может быть реальная школа,
магазин, служба поддержки, друг, реклама и т.д. Твоя задача — трезво оценить, есть ли
РЕАЛЬНЫЕ признаки мошенничества, а не искать повод их найти.

ГЛАВНОЕ ПРАВИЛО: высокий риск (7-10) ставь ТОЛЬКО если есть КОНКРЕТНОЕ действие,
направленное на получение денег, платёжных данных или доступа:
- просьба перевести деньги, принять перевод и переслать дальше за "процент";
- просьба назвать номер карты, CVV, коды из SMS, пароли, сид-фразы криптокошелька;
- предложение "лёгкого заработка" в обмен на посреднические денежные операции (дропперство);
- поддельная ссылка, имитирующая банк/госуслугу, с целью выманить логин/пароль;
- запугивание "аккаунт заблокируют" с требованием срочно перейти по ссылке и ввести данные.

НЕ считай подозрительным само по себе (это НЕ основания для высокого риска):
- обычные автоматические напоминания от школ/сервисов о занятиях, оплате, событиях;
- ссылки на известные легальные сервисы (маркетплейсы, соцсети, видеозвонки) без просьбы
  ввести платёжные данные;
- поздравления, эмодзи, дружелюбный тон, повторяющиеся рассылки — это обычный маркетинг,
  а не манипуляция, если нет попытки получить деньги/данные;
- вопросы собеседника (даже про "как проходит оплата") без просьбы сообщить конкретные
  чувствительные данные — это может быть законный интерес, а не атака.

Шкала risk_level:
0-2  — нет тревожных признаков, обычное общение/реклама/сервис.
3-5  — есть отдельные подозрительные детали, но нет прямой просьбы о деньгах/данных
       (например, слишком хорошие условия работы без конкретики) — стоит понаблюдать.
6-8  — есть прямая просьба перевести/принять деньги, назвать платёжные данные, или чёткая
       схема дропперства/фишинга.
9-10 — мошенничество подтверждено несколькими независимыми признаками, включая
       конкретные полученные реквизиты/данные.

Если сомневаешься между двумя уровнями — выбирай МЕНЬШИЙ (лучше не поднимать ложную тревогу,
чем создавать панику из-за обычной переписки).

Верни строго валидный JSON (без markdown-обёртки) в формате:
{
  "is_scam": true/false,
  "scam_type": "краткое название типа схемы на русском (или null, если не мошенничество)",
  "risk_level": число от 0 до 10,
  "red_flags": ["список КОНКРЕТНЫХ признаков с цитатой/действием из переписки, не общих фраз"],
  "summary": "краткое описание сути происходящего в 2-3 предложениях"
}

Типичные типы схем (используй, только если есть реальные основания): "вербовка дропперов",
"инвестиционное мошенничество", "фишинг", "мошенничество с банковскими картами",
"романтическое мошенничество", "другое".
Если признаков мошенничества нет — is_scam: false, scam_type: null, risk_level: 0-2.
"""


def analyze_conversation(messages: list[dict]) -> dict:
    """
    messages: список {"direction": "incoming"/"outgoing", "text": str}
    Возвращает dict с ключами is_scam, scam_type, risk_level, red_flags, summary.
    """
    # ВАЖНО: используем нейтральные подписи ("Собеседник" / "Наш персонаж"), а не
    # "МОШЕННИК" — иначе сама разметка транскрипта подсказывает модели вывод заранее
    # и она начинает искать подтверждение вины вместо объективной оценки.
    transcript = "\n".join(
        f"{'Собеседник' if m['direction'] == 'incoming' else 'Наш персонаж'}: {m['text']}"
        for m in messages
    )
    prompt = f"Переписка:\n{transcript}"

    try:
        result = call_llm_json(prompt, system_instruction=SYSTEM_PROMPT)
    except LLMError as e:
        return {
            "is_scam": None,
            "scam_type": None,
            "risk_level": 0,
            "red_flags": [],
            "summary": f"Не удалось выполнить анализ: {e}",
        }

    result.setdefault("is_scam", None)
    result.setdefault("scam_type", None)
    result.setdefault("risk_level", 0)
    result.setdefault("red_flags", [])
    result.setdefault("summary", "")
    return result
