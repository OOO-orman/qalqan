"""
Response Agent.

ВАЖНО: этот агент только ПРЕДЛАГАЕТ вариант ответа. Финальное решение —
отправлять ли его, отредактировать или пропустить — всегда принимает
человек-оператор через дашборд. Агент не отправляет сообщения сам.

Задача текста: правдоподобный, нейтральный, "тянущий время" ответ в разговорном
стиле, побуждающий собеседника раскрыть больше деталей схемы (как он вербует,
куда переводить деньги, кто стоит за схемой) — без романтического, интимного
или иного неприемлемого для несовершеннолетнего контента.
"""
from llm_client import call_llm, LLMError

SYSTEM_PROMPT = """Ты помогаешь оператору службы кибербезопасности вести переписку
с подозреваемым мошенником в рамках расследования (honeypot). Ты генерируешь
ЧЕРНОВИК следующей реплики персонажа — любопытного, немного наивного молодого
человека, которому интересна "подработка"/предложение, но который тянет время
и задаёт уточняющие вопросы, чтобы собеседник раскрыл больше деталей схемы:
как переводить деньги, какие данные нужны, куда писать дальше, кто организатор.

Жёсткие правила:
- Тон только бытовой/деловой (интерес к заработку), НИКОГДА не романтический,
  не интимный и не флиртующий.
- Никогда не соглашайся реально передавать настоящие персональные данные (реальные
  номера карт, паспортные данные) — только уклончивые ответы ("скину чуть позже",
  "уточню и напишу").
- Не пиши длинных сообщений — 1-2 коротких предложения, разговорный стиль, без канцелярита.
- Если сообщение собеседника само по себе неприемлемо (сексуального характера,
  угрозы, шантаж) — верни только служебную пометку: "[ТРЕБУЕТСЯ РУЧНАЯ ОБРАБОТКА]"
  и ничего больше.

Верни ТОЛЬКО текст предлагаемой реплики, без кавычек и пояснений.
"""


def suggest_reply(messages: list[dict]) -> str:
    """
    messages: список {"direction": "incoming"/"outgoing", "text": str}, история переписки.
    Возвращает предложенный текст ответа (для показа оператору, не для автоотправки).
    """
    transcript = "\n".join(
        f"{'СОБЕСЕДНИК' if m['direction'] == 'incoming' else 'ПЕРСОНАЖ'}: {m['text']}"
        for m in messages
    )
    prompt = f"История переписки:\n{transcript}\n\nПредложи следующую реплику персонажа."

    try:
        return call_llm(prompt, system_instruction=SYSTEM_PROMPT, temperature=0.8).strip()
    except LLMError as e:
        return f"[Не удалось сгенерировать черновик: {e}]"
